//---------------------------------------------------------------------
package studentOrientation.orientation.concreteClasses;
//---------------------------------------------------------------------
import studentOrientation.orientation.abstractClasses.AbstractBookstore;
//---------------------------------------------------------------------
public class MandoBooks extends AbstractBookstore
{
	private String name;
	private int time;
	private double cost;
	private int effort;

	//Constructor class sets values for the option
	public MandoBooks(String name, int time, double cost, int effort)
	{
		this.name = name;
		this.time = time;
		this.cost = cost;
		this.effort = effort;
	}

	/**
	*	This is the getter method for the cost
	*
	*	@return Returns the activity's name
	**/
	public String getName()
	{
		return name;
	}

	/**
	*	This is the getter method for the cost
	*	@return Returns the activity's time
	**/
	public int getTime()
	{
		return time;
	}

	/**
	*	This is the getter method for the cost
	*	@return Returns the activity's cost
	**/
	public double getCost()
	{
		return cost;
	}
    
	/**
	*	This is the getter method for the effort
	*	@return Return the activity's effort
	**/
	public int getEffort()
	{
		return effort;
	}
	
	public void setName(String name)
	{
		this.name = name;
	}

	public void setTime(int time)
	{
		this.time = time;
	}

	public void setCost(double cost)
	{
		this.cost = cost;
	}

	public void setEffort(int effort)
	{
		this.effort = effort;
	}

	/**
	*	This is the overridden toString method for this class
	*	@return Returns the class details as a string
	**/
	@Override
	public String toString() 
	{
		return name +"- Time: "+ time +" Cost: "+ cost 
			+" Calories: "+effort;
	}
}
    
